export type Lang = "en" | "cn" | "es";

export const getInitialLang = (): Lang => {
  if (typeof navigator !== "undefined") {
    const lang = navigator.language.toLowerCase();
    if (lang.startsWith("zh")) return "cn";
    if (lang.startsWith("es")) return "es";
  }
  return "en";
};

